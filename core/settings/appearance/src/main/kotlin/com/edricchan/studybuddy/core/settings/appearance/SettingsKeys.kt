package com.edricchan.studybuddy.core.settings.appearance

/**
 * Key for the "enable custom tabs" preference.
 *
 * The preference's value is a [Boolean] which determines whether
 * Chrome Custom Tabs should be used when opening links.
 */
const val keyPrefUseCustomTabs = "pref_use_custom_tabs"

/**
 * Key for the "enable dark theme" preference.
 *
 * The preference's value is a [Boolean] which determines whether
 * dark theme should be applied across the application.
 */
const val keyPrefDarkTheme = "pref_dark_theme"

/**
 * Key for the "enable dynamic theme" preference.
 *
 * The preference's value is a [Boolean] which determines whether
 * system-wide theming introduced in Android 12 should be applied
 * across the application.
 */
const val keyPrefDynamicTheme = "pref_dynamic_theme"
